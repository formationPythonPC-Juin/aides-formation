import matplotlib.pyplot as plt
from math import pi
import numpy as np


######################################
# .......
######################################
I0 = 3
phi = pi/2
k = pi
omega  = pi
T = 2*pi/omega


######################################
# ..........
######################################
debut = 0
fin = 5
pas = 0.01
x = np.arange(debut, fin+pas, pas) # .........

t = 0 # .......
I = I0*np.cos(omega*t-k*x+phi) # .......


####################################
# ................
#####################################
courbe, = plt.plot([],[],'b-') # .......
plt.xlim(0,5) # .......
plt.ylim(-I0,I0) # .......
plt.grid() # .......



####################################
# ................
#####################################

dt = T/50 # .......

while t < 3*T : # ........
    I = I0*np.cos(omega*t-k*x+phi) # .......
    courbe.set_data(x,I) # .......
    print(t) # .......
    plt.pause(dt) # .......
    t = t + dt # .......

plt.show() # .......
